# 2.2.1. Завдання на лінійну послідовність 
# Обчислити площу круга A та довжину кола C за його радіусом R 
def circle_calc_by_R(R: float) -> tuple:
    #перевірка на коректність вхідних даних
    if R <= 0:
        return "!!! радіус повинен бути невід'ємний", None
    
    #обчислення площі круга A = Pi * R^2
    area = math.pi * (R ** 2)
    
    #обчислення довжини кола C = 2 * Pi * R
    circumf = 2 * math.pi * R
    
    return area, circumf


