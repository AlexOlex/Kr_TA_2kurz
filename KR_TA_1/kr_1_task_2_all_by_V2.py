# Перевірити, чи належить точка (x, y) першій I або третій III координатній чверті
def check_q1_q3(x: float, y: float) -> str:
    #точка лежить на осі координат
    if x == 0 or y == 0:
        return "точка знаходиться на осі"
        
    #умова належності I чверті: x > 0 та y > 0
    q1 = (x > 0 and y > 0)
    
    #умова належності III чверті: x < 0 та y < 0
    q3 = (x < 0 and y < 0)
    
    #перевіряємо чи належить точка I або III чверті
    if q1 or q3:
        #визначаємо яка чверть
        quadrant = "I" if q1 else "III"
        return f"чверть {quadrant}"
    else:
        #точка належить II або IV чверті
        return "чверть II/IV"


# 2.2.3. Завдання на цикл for 
# Вивести добуток чисел від A до B (включно)

def range_product(A: int, B: int) -> int | str:
    if A > B:
        return "!!! число A повинно бути <= B"
        
    #ініціалізація добутку
    product = 1
    
    for number in range(A, B + 1): #від A до B включно
        product *= number
    # повертаємо накопичений добуток
    return product


# 2.2.4. Завдання на цикл while 
# Обчислити суму цифр цілого числа N

def sum_of_digits_while(N: int) -> int:
   
    N_abs = abs(N)  #використовуємо абсолютне значенням числа N 
    
    #ініціалізація суми цифр
    digit_sum = 0

    while N_abs > 0: # цикл while  продовжуємо доки число не стане нулем
        #1 - беремо останню цифру-залишок від ділення на 10
        digit = N_abs % 10
        
        #2 - додаємо цифру до загальної суми
        digit_sum += digit
        
        #3 -відкидаємо останню цифру (цілочисельне ділення на 10)
        N_abs //= 10
        
    #повертаємо залишкову суму
    return digit_sum

